// Faça um algoritmo que leia três notas de um aluno, calcule e escreva a
// média final deste aluno. Considerar que a média é ponderada e que o
// peso das notas é 2, 3 e 5. Fórmula para o cálculo da média final é:
//                  n1 * 2 + n2 * 3 + n3 * 5
//  média final = -----------------------------------
//                            10
var nota1 = 5*2
var nota2 = 9*3
var nota3 = 5*5
 
media = (nota1+nota2+nota3) /10
console.log(`Á média final dos aluno é: ${media}`)